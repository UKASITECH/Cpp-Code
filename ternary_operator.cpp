#include <iostream>
using namespace std;

int main()
{
    cout << "Hello, World!";
    // Ternary Operator is also called short-hand if else
    // we can write the if else instead in a short way using ternary operator

    int x = 10;
    int y = (x > 5) ? 20 : 30; // y will be assigned 20 because x > 5 is true
    return 0;
}